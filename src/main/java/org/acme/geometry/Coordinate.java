package org.acme.geometry;

public class Coordinate {
    private static double x;
    private static double y;

    public Coordinate(double x, double y){
        this.x = 0.0;
        this.y = 0.0; 

    }

    public static getX(double x){
        return x;
    }

    public static getY(double y){
        return x;
    }
}
